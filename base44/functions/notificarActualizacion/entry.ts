Deno.serve(async (req) => {
  try {
    const base44 = createClientFromRequest(req);
    const { persona_id, tipo_evento, datos_persona } = await req.json();

    if (!persona_id) return Response.json({ error: 'persona_id requerido' }, { status: 400 });

    const subs = await base44.asServiceRole.entities.Suscripciones.filter({ persona_id, activa: true });
    if (!subs || subs.length === 0) return Response.json({ ok: true, notificados: 0 });

    const nombre = datos_persona?.nombre_completo || 'La persona';
    const estadoCaso = datos_persona?.estado_caso || 'buscando';
    const ubicacion = datos_persona?.ubicacion || '';

    const esNueva = tipo_evento === 'nueva_busqueda';
    const esEncontrada = tipo_evento === 'persona_encontrada';

    const asuntoEs = esNueva
      ? `🔎 Nueva búsqueda registrada: ${nombre}`
      : esEncontrada
        ? `📋 Hay información sobre ${nombre}`
        : `${ESTADO_EMOJI[estadoCaso] || '📋'} Actualización: ${nombre}`;

    const asuntoEn = esNueva
      ? `🔎 New search registered: ${nombre}`
      : esEncontrada
        ? `📋 There is information about ${nombre}`
        : `${ESTADO_EMOJI[estadoCaso] || '📋'} Update: ${nombre}`;

    const APP_URL = 'https://app.statusvzla.com';

    let notificados = 0;
    for (const sub of subs) {
      const token = genToken();
      const expira = new Date(Date.now() + 7 * 24 * 60 * 60 * 1000).toISOString();
      await base44.asServiceRole.entities.TokensAutoUpdate.create({
        token, persona_id, email_destino: sub.email_notificacion, usado: false, expira,
      });

      const updateLink = `${APP_URL}/actualizar-estado?token=${token}`;
      const profileLink = `${APP_URL}/persona?id=${persona_id}&token=${token}`;
      const lang = 'es'; // default to Spanish for Venezuela context

      const htmlBody = buildEmailHtml({
        nombre,
        estadoCaso: esNueva ? 'buscando' : estadoCaso,
        ubicacion,
        mensaje_extra: datos_persona?.mensaje || '',
        updateLink,
        profileLink,
        lang,
      });

      const textBody = `${asuntoEs}\n\n${nombre} — ${ESTADO_LABEL_ES[estadoCaso] || estadoCaso}\n\n${ubicacion ? `📍 ${ubicacion}\n\n` : ''}Si eres ${nombre} o puedes actualizar esta información:\n${updateLink}\n\n⚠️ Nunca envíes dinero a cambio de información. STATUSVZLA.com no autoriza pagos ni rescates privados.\n\n— Equipo STATUSVZLA.com`;

      await base44.asServiceRole.integrations.Core.SendEmail({
        to: sub.email_notificacion,
        subject: asuntoEs,
        body: htmlBody,
        from_name: 'STATUSVZLA.com',
      });

      await base44.asServiceRole.entities.NotificacionesUsuario.create({
        user_id: sub.user_id,
        tipo: tipo_evento,
        titulo: asuntoEs,
        mensaje: textBody,
        leida: false,
        ref_id: persona_id,
        link_ref: `/actualizar-estado?token=${token}`,
      });

      notificados++;
    }

    return Response.json({ ok: true, notificados });
  } catch (error) {
    return Response.json({ error: error.message }, { status: 500 });
  }
});